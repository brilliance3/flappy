import { useSyncExternalStore } from "react";

/**
 * Lightweight i18n that follows the SimsimPlay portal language.
 *
 * The portal's embed.js stores the chosen language in localStorage under
 * "ssp_lang" (en/ko/ja/zh/es/fr/de, default "en") and — importantly — does NOT
 * dispatch any event or reload the page when it changes. So we mirror that key:
 * read it on load, listen for cross-tab "storage" events, and poll for same-tab
 * changes. `t()` is a pure string lookup; game logic is never touched.
 */

export type Lang = "en" | "ko" | "ja" | "zh" | "es" | "fr" | "de";

export const SUPPORTED: Lang[] = ["en", "ko", "ja", "zh", "es", "fr", "de"];

const STORAGE_KEY = "ssp_lang";

function readLang(): Lang {
  try {
    const s = localStorage.getItem(STORAGE_KEY);
    if (s && (SUPPORTED as string[]).includes(s)) return s as Lang;
  } catch {
    /* ignore (private mode / blocked storage) */
  }
  return "en";
}

let current: Lang = readLang();
const listeners = new Set<() => void>();

export function getLang(): Lang {
  return current;
}

function setCurrent(next: Lang): void {
  if (next === current) return;
  current = next;
  listeners.forEach((fn) => fn());
}

function subscribe(fn: () => void): () => void {
  listeners.add(fn);
  return () => listeners.delete(fn);
}

let syncStarted = false;
/** Begin mirroring the portal's language choice. Call once at startup. */
export function startLangSync(): void {
  if (syncStarted || typeof window === "undefined") return;
  syncStarted = true;
  window.addEventListener("storage", (e) => {
    if (e.key === STORAGE_KEY) setCurrent(readLang());
  });
  // embed.js mutates localStorage without firing an event in the same tab.
  window.setInterval(() => setCurrent(readLang()), 400);
}

/** React hook: re-renders the component when the portal language changes. */
export function useLang(): Lang {
  return useSyncExternalStore(subscribe, getLang, getLang);
}

